$(document).ready(function () {
    $.HesapMakinesi = {
        Toplama: function (a, b) {
            return a + b;
        },
        Cikarma: function (a, b) {
            return a - b;
        },
        Carpma: function (a, b) {
            return a * b;
        },
        Bolme: function (a, b) {
            return a / b;
        }
    };
});